/// Busca la traducción al español de un gloss en inglés.
/// Busca primero en el diccionario griego, luego en el hebreo.
/// Si no encuentra, devuelve el gloss original.
String translateGloss(String englishGloss, {bool isOT = false}) {
  final lower = englishGloss.toLowerCase().trim();

  // Buscar el diccionario primario según el testamento
  final primary = isOT ? kHebrewGlossToSpanish : kGreekGlossToSpanish;
  final secondary = isOT ? kGreekGlossToSpanish : kHebrewGlossToSpanish;

  if (primary.containsKey(lower)) return primary[lower]!;
  if (secondary.containsKey(lower)) return secondary[lower]!;

  // Intentar con la primera palabra si es una frase
  final firstWord = lower.split(' ').first;
  if (firstWord != lower) {
    if (primary.containsKey(firstWord)) return primary[firstWord]!;
    if (secondary.containsKey(firstWord)) return secondary[firstWord]!;
  }

  return englishGloss;
}
